--- ORDER BY ---

SELECT placa, km
FROM VEICULOS
ORDER BY km;
SELECT placa, km
FROM VEICULOS
WHERE ano > 2000
ORDER BY km DESC; -- DECRESCENTE - MAIOR PARA MENOR

--- COUNT --- CONTANDO REGISTROS

SELECT COUNT(*)
FROM VEICULOS
WHERE km = 0;

SELECT COUNT(*) FROM VEICULOS
WHERE marca = 'Ford'

--- DISTINCT --- TRAZ OS REGISTRO DIFERENTES DA COLUNA

SELECT DISTINCT marca
FROM VEICULOS;

