--- CRIAÇÃO DA TABELAS
CREATE TABLE VEICULOS
(
placa CHAR(8),
ano NUMBER(4),
km NUMBER(6),
marca VARCHAR(50),
modelo VARCHAR(50)
);

--- INSERÇÃO DE DADOS
